// If else Practice

let characterLevel = 15;
const mLevel = characterLevel;
if (mLevel < 20) {
  console.log("Your dungeon level is low, level up for more challenges!");
} else if (mLevel < 50 && mLevel > 20) {
  console.log(
    "Congratulations you are a the mid level section of your adventure, use your skills to progress and overcome your challenges and take reach  the upper eschelons of power!"
  );
} else if (mLevel > 70) {
  console.log(
    "You have reached a high level of existence that only the tenacious could acheive. Be happy with your accomplishemnts and adventure to your hearts content!"
  );
}
